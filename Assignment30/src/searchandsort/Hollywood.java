package searchandsort;

public class Hollywood {

	private String name;
	private int score;
	
	
	
	public Hollywood(String name, int score) {
		this.name=name;
		this.score=score;
		
	}
	
	//getter
	public String getName() {
		
		return this.name;
		
	}
	
	
	public int getScore() {
		
		return this.score;
		
	}
	
	
	
}
